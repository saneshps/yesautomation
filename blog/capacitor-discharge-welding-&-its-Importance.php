                  <h2>What is a Capacitor-Discharge Welding system?</h2>


                  <p>A Capacitor-Discharge Welding or CD Welding is the fastest form of resistance welding & utilizes capacitors to deliver power. In this welding system the capacitors are charged with a large amount of energy, and then this charged energy is rapidly released into the parts within milliseconds.</p>

                 
	              <p><a href="https://www.yesautomation.ae">YES Automation</a> offer’s CD stud welding machines with high strength, speed, and reliability from a simple process and welding system.</p>



	              <p>For a closer look at the steps in the capacitor discharge stud welding machine, the welding process occurs in 4 steps. </p>
				  <p>They are; </p>       

	              <ol>

	              	<li><b>Contact</b></li>
	              	<li><b>Ignition</b></li>
	              	<li><b>Placement (or ‘closing the Gap’)</b></li>
	              	<li><b>Completion of the Weld Bond</b></li>


	              </ol>

	              <p>At first, the Stud Gun allows ‘contact’ with the material.</p>

					<p>Then the CD gun is triggered passing the weld charge through the Gun, giving ignition of the stud at the stud tip.</p>

					<p>At the third step, the “tip” melts, a small area of the material under the head of the fastener also starts to melt. The Capacitor Discharge gun forces the stud into the melted material, closing the gap between fastener and material, and “landing” the fastener properly.</p>

					<p>In the last step, the stud is held in place as it instantly bonds together with the base material.</p>


