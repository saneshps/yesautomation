	              <h2>What is Stud Welding?</h2>
	              <p>Stud welding is a single-side fastening method that joins two metal parts, in that a metal sheet is known as the parent material. Parts are fused together employing a strong arc of electricity created by a positive current. The arc generates enough heat for both metals to succeed in their melting points. Once a molten pool forms, the part, and parent material are pressed together to make a secure joining and become one.</p>

	              <p>Stud welding is popular partly due to how quickly it is often administered. With the proper equipment, workers can end up a stud weld in milliseconds. The speed, however, doesn't affect the strength and reliability of the resulting joints. they're actually stronger than the parent material itself once formed and can last a lifetime.</p>
	
