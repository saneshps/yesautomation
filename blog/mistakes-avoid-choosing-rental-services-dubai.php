	              <h2>Never ignore any safety instructions</h2>

	              <p>Safety always comes first when you work with machines, tools and equipment and there is no argument to it. You need to remember that all the machines and equipment comes with printed instructions from the manufacturer. Take time to study those instructions and follow it in order to prevent any kind of accidents or damage.</p>

	              <p> Another major thing to remember is that some equipment or tools have specific requirements related to safety. It’s your responsibility to properly understand it and operate accordingly. Last but not least, wear proper safety gears like gloves, goggles, helmets as mentioned in the manual.</p>

	              <h2>Understand Rental Agreements</h2>

	              <p>It’s a normal practice to sign a rental agreement before you rent any machinery. It’s your responsibility again to fully read and understand the agreement before you sign it.  Rental agreements normally include details regarding the time frame of the machinery or equipment that is given to you for rent, rental charges, deposit (if any ) associated with that rental etc.</p>

	              <p>If you feel that any part of the agreement is not clear to you, you should check with your rental service provider and try to understand and clear  all your doubts before signing. This will help you avoid any surprises during the time of rental machine return.</p>

	              <h2>Make Proper Planning</h2>

	              <p>Planning your things properly and well ahead will help you save money, confusion and time. While renting equipment, take into consideration the duration of the project and make a realistic date for returning the equipment. Also, it's always good to foresee any unexpected situations which can lead you to extending the rental period. Availing rental services after careful planning will make sure that the equipment is with you during the time you need it and you wont incur any extra charges for extension.</p>

	              <h2>Non-Maintenance of Rented Equipment</h2>
	              <p>Properly doing maintenance for the equipment or machinery during the rented period is important to ensure safety as well as its proper functioning. Make sure you clean the machinery on regular intervals and place it in safe storage areas while not in operation. When you return a rented equipment in the same good condition as you took it initially, it will save you from extra costs.</p>

	              <h2>Rental Insurance</h2>

	              <p>It;s a good practice to take insurance on high valued rental machines or if you are working with rental machines on huge projects. Having rental insurance can protect you from being accountable for huge damages to the equipment and the need to cover it, Insurances can be taken either as liability insurance or as damage insurance. While availing rental services, it;s good to understand the scope as well as risks involved in the project, magnitude of the project and evaluate where or not you need insurance for your rented machines.</p>

