	              <ul>

	              	<li><b>Health Hazards Anticipation</b></li>

	              </ul>

	              <p>To keep the workplace safe from all hazards, the Anticipation of Health Hazards is necessary as the first principle of industrial hygiene. In this principle, to collect information about the design of the workplace, operations, processes, work tasks, materials, and the population of the workers a survey is conducted.</p>

	              <p>For the quick identification of all the chemical hazards in the workplace, we have the current inventory of chemicals and their Safety Data Sheets. to prioritize which hazards need to be investigated we can analyze the hazardous properties of each chemical onsite.</p>

	              <p>Safety data sheets are documents that contain a list of the properties of each chemical an employee could come into contact with. With the help of Safety data sheets, we can identify potential hazards.</p>

	              <p>Industry owners should always double-check their scrap suppliers whether it is reliable and are following the established guidelines and should obtain Safety data sheets and labels for the scrap materials where available.</p>

	              <p>During this phase, we have identified if there is any potential exposure like employees working in an environment where there is greater exposure and no proper ventilation or places where the workers are exposed to huge amounts of dust.</p>

	              <p>In nonferrous recycling industries workers used to suffer from illness caused by poisoning from materials like lead or cadmium, repeated trauma disorders, diseases or disorders affected to the skin, respiratory problems due to breathing toxic agents, or problems caused by coming in contact with toxic substances. Like this, we have to anticipate each industry's health hazards.</p>

