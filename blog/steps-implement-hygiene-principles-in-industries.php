	              <h3>What are occupational health and safety principles?</h3>

	              <p>There are three fundamental rights that are entitled to all employees by the Occupational Health and Safety Act. The first one is the right to know about health and safety affairs. The second right is to partake in decisions that could impact their health and safety. And the last and third one is to reject work that could affect their health and safety and that of others.</p>

	              <h3>How do you explain the role of occupational hygiene?</h3>

	              <p>Role: Assist employers, Improve employee workplace conditions, Improve work practices 
	              <p>Through: Quantifying exposure levels, Assessing the risk to employees, Recommending control measures to minimize exposure in accordance with legislation.</p>

	              <h3>What are 3 occupational hygiene components?</h3>

	              <p>Identification of potential health hazards</p>
	              <p>Evaluation of workplace health hazards.</p>
	              <p>Control of workplace health hazards.</p>

	              <p>These are the main three components of occupational hygiene. The first step in a prevention program is the identification of potential health hazards. And then Labels, MSDSs, and walk-through inspections are done.</p>



