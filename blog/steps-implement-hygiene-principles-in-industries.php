	              <ul>

	              	<li><b>Health Hazards Recognition</b></li>

	              </ul>

	              <p>The second principle of industrial hygiene is the recognition of health hazards which is very important to recognize and understand the potential hazards. In this principle, potential hazards of the work environment, internal processes,  job tasks, chemical hazards, and physical hazards at a workplace are recognized and comprehended. For example, there are some employees who work in an area with poor ventilation and have greater exposure to occupational hazards.</p> 

	              <p>Some employees' work tasks generate a lot of dust and do work shifts that can last for a long time.  individual work tasks also include in this and how these tasks will affect alike work activities that could create exposure to hazards for other workers in the facility which are also referred to as simultaneous operations. There are a lot of hazardous industrial issues to be recognized in this principle.</p>

	              <ul>

	              	<li><b>Health Hazards Evaluation</b></li>

	              </ul>

	              <p>Evaluation of exposure as the third principle of industrial hygiene, the primary means of reducing employee exposure to occupational hazards are considered engineering, work practice, and administrative controls. Now it’s time to evaluate the risk of exposure through an Occupational Exposure assessment after anticipating and recognizing your health hazards.</p>

	              <p>In the olden days' evaluation was referred to as workplace monitoring. And this monitoring could only be done by a qualified professional like an industrial or occupational hygienist since the work environment and the measuring instruments were all complex.</p>

	              <h3>Monitoring the Industrial Hygiene</h3>

	              <p>To catch the exposure level of a particular population of workers at a certain time of their work activities we need Industrial hygiene monitoring. Where this measurement can be conducted? It is on those with the highest potential exposure, or a representative exposure. This measurement can also refer to a similar exposure group (SEG), that is workers who perform the same tasks with similar materials, at the same frequency. a specific physical, biological, or chemical agent can be measured by Industrial hygiene monitoring, where the exposure occurs.</p>

	              <p>The variations in exposure are also considered by monitoring during different work shifts or facility maintenance activities like turnarounds. Monitoring is conducted to have a defined purpose or question it is trying to answer. To determine, for the hazardous product, if the exposure has happened safely over or below the OEL (occupational exposure limits). Or to determine if exposure results are acceptable, unacceptable, or uncertain through the qualitative and quantitative analysis of data from monitoring.</p>

	              <ul>

	              	<li><b>Control Over Worker Exposure</b></li>

	              </ul>

	              <p>The fourth principle of industrial hygiene is Controlling worker exposure. controls need to be implemented to protect workers when hazardous exposure is determined to be unacceptable.  To guide decisions on a multifaceted approach to exposure control, the hierarchy of controls should be used. Once elimination, substitution, engineering controls, and administrative practices are considered then only you can apply control over the Personal Protective Equipment or PPE first.</p>

	              <ul>

	              	<li><b>Control Measure’s Confirmation</b></li>

	              </ul>

	              <p>The last and fifth principle of industrial hygiene is the confirmation of Control Measures. This fundamental of industrial hygiene is to assess the performance of hazard control measures and followed by the worker exposure to the hazards. After Investigating existing or potential issues it is necessary to apply corrective actions and this is also a part of confirming that your industrial hygiene program is effective. The key to tracking and trend the success of your industrial hygiene program is maintaining data and reports to prevent injury, illness, or negative effects on the well-being of workers.</p>

