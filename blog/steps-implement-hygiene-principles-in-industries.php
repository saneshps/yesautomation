	              <p>There are several components that contribute to industrial hygiene programs that include a written policy/program statement, procedure of hazard recognition, assessment of hazard evaluation and exposure, hazard control, training the employee, employee involvement, evaluation and audit of the program, and record keeping.</p>

	              <h2>5 Principles Of Industrial Hygiene</h2>

	              The 5 basic principles or fundamentals of industrial hygiene to prevent injury and illness applied by occupational hygienists are ;

	              <ul>

	              	<li><b><strong>1)Anticipation</strong></b></li>

	              </ul>

	              <ul>

